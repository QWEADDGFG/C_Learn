int main(void) {
	int i;
	float f;

	// 整个表达式的值：赋值后，变量i的值。
	i = 3.14f;	// 计算表达式的时候，可能会发生副作用


	f = (i = 3.14f);

	printf("i = %d, f = %.2f\n", i, f);	// i = 3, f = 3.00
	return 0;
}